package com.ibm.AdditionDao;

public class Sum {

	int a;
	int b;
	String c;
	public String getC() {
		return c;
	}
	public void setC(String c) {
		this.c = c;
	}
	public int getA() {
		return a;
	}
	public void setA(int a) {
		this.a = a;
	}
	public int getB() {
		return b;
	}
	public void setB(int b) {
		this.b = b;
	}
	public int add() {
		return a+b;
	}
	
}
